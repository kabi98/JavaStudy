package ex03for문;

public class Ex01for {
	public static void main(String[] args) {
		System.out.println("*** Start ***");
//////////////////////////////////////////////////////////////////////////////////
// while문 : 대개 반복횟수가 정해져 있지 않을 때 
// for문   : 대개 반복횟수가 정해져 있을 때
		for (int i = 1; i <= 10; i++) {
			System.out.println("출력 : " + i);
		}

//////////////////////////////////////////////////////////////////////////////////
		System.out.println("*** Finish ***");
	}

}
